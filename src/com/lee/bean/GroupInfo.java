package com.lee.bean;

public class GroupInfo {
	private int groupid;
	private String groupname;
	private String portrait;
	private int status;
	private String introduce;
	private int number;
	private int max_number;
	private int create_user_id;
	private String creat_datetime;
	
	public int getGroupid() {
		return groupid;
	}
	public void setGroupid(int groupid) {
		this.groupid = groupid;
	}
	public String getGroupname() {
		return groupname;
	}
	public void setGroupname(String groupname) {
		this.groupname = groupname;
	}
	public String getPortrait() {
		return portrait;
	}
	public void setPortrait(String portrait) {
		this.portrait = portrait;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getIntroduce() {
		return introduce;
	}
	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int num) {
		this.number = num;
	}
	public int getMax_number() {
		return max_number;
	}
	public void setMax_number(int max_number) {
		this.max_number = max_number;
	}
	public int getCreate_user_id() {
		return create_user_id;
	}
	public void setCreate_user_id(int create_user_id) {
		this.create_user_id = create_user_id;
	}
	public String getCreat_datetime() {
		return creat_datetime;
	}
	public void setCreat_datetime(String creat_datetime) {
		this.creat_datetime = creat_datetime;
	}
	
	@Override
	public String toString() {
		return "GroupInfo [groupid=" + groupid + ", groupname=" + groupname + ", portrait=" + portrait + ", status="
				+ status +  ", introduce=" + introduce + ", number=" + number + ", max_number="
				+ max_number + ", create_user_id=" + create_user_id + ", creat_datetime=" + creat_datetime + "]";
	}
	
}
